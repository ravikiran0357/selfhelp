package com.zyppysselfhelp.bean;

public class ContentRequest {

	private String contentSource;
	private String link;
	private String language;
	private String hashtags;
	private String title;
	private String contentFormatType;
	private String contentLookupCategory;
	private String instructor;
	private String isPremium;

	public String getContentSource() {
		return contentSource;
	}

	public void setContentSource(String contentSource) {
		this.contentSource = contentSource;
	}

	public String getLink() {
		return link;
	}

	public void setLink(String link) {
		this.link = link;
	}

	public String getLanguage() {
		return language;
	}

	public void setLanguage(String language) {
		this.language = language;
	}

	public String getHashtags() {
		return hashtags;
	}

	public void setHashtags(String hashtags) {
		this.hashtags = hashtags;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getContentFormatType() {
		return contentFormatType;
	}

	public void setContentFormatType(String contentFormatType) {
		this.contentFormatType = contentFormatType;
	}

	public String getContentLookupCategory() {
		return contentLookupCategory;
	}

	public void setContentLookupCategory(String contentLookupCategory) {
		this.contentLookupCategory = contentLookupCategory;
	}

	public String getInstructor() {
		return instructor;
	}

	public void setInstructor(String instructor) {
		this.instructor = instructor;
	}

	public String getIsPremium() {
		return isPremium;
	}

	public void setIsPremium(String isPremium) {
		this.isPremium = isPremium;
	}

}
